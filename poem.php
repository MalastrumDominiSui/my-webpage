<h2>
It was after we passed Moby’s Dock<br>
that Ebony met her first thresher shark<br>
<br>
He was five feet long or so<br>
two feet shark, three feet tail,<br>
and had just been pulled from the surf<br>
to be proudly displayed<br>
by the fisherman who had caught him<br>
<br>
Ebony stood transfixed<br>
her every muscle poised<br>
her feathered tail twitched<br>
as she leaned closer to inspect<br>
and then recoiled from this cold-blooded beauty<br>
still dressed in fleetingly iridescent <br>
blues and greens and purples - <br>
<br>
As the sun’s fading beams highlighted<br>
the magnificence of this dying shark<br>
I mourned his loss that night.<br>
<br>
The noise and tourists<br>
in the Pier’s arcades and bumper cars<br>
did not detract from the peacefulness<br>
of the Pacific in her chaos<br>
for this was August<br>
and they would soon go home<br>
<br>
I watched a distant storm at sea<br>
flashing fire against the deepening twilight<br>
I stood, and Ebony, <br>
gazing at the flashes of lightning<br>
<br>
as I stroked the waves of her black fur<br>
relishing the cool wind on my face<br>
listening to the rigging <br>
of the boats resting at anchor off the Pier<br>
<br>
Thinking about thresher sharks<br>
Willing them away<br>
from this place with its fishermen<br>
and cold, baited hooks<br>
<br>
Cori MacNaughton<br>
13 Sept 2000<br>
</h2>

